// 2741번 N 찍기
package BOJ_2741;

import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        int n;
        Scanner sc = new Scanner(System.in);
        n = sc.nextInt();

        for (int i=1; i<n+1; i++) {
            System.out.println(i);
        }
    }
}
